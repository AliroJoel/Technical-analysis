def clark_west_y_r2_para_x(
    df: pd.DataFrame,
    x_var: str,
    dep_col: str = 'y_f',
    start_ratio: float = START_RATIO,
    min_obs: int = MIN_OBS,
    hac_lags: int = HAC_LAGS
):
    """
    Compute Clark & West (one-sided) and OOS R² for a single predictor `x_var`
    against a target column `dep_col` using an expanding window.

    Models:
      Core   : dep_col ~ const + x_var
      Bench  : dep_col ~ const    (historical mean)

    Steps:
      1) Keep only the necessary columns and clean Inf/NaN.
      2) Determine the first out-of-sample index from `start_ratio` (respecting `min_obs`).
      3) For each expanding step:
         - Estimate OLS on Core and Bench with all data up to j-1.
         - Predict at j for both models.
      4) After the loop, compute:
         - MSE(core) and MSE(bench)
         - OOS R² := 1 - MSE(core) / MSE(bench)
         - Clark–West t-stat via HAC( maxlags = hac_lags )
      5) Return a result dict and simple diagnostics (n valid, n OOS).

    Returns
    -------
    (result_dict, error_msg, n_valid, n_oos)
      - result_dict: {'dependent','Variable','OOS_R2','CW_t'} or None on error
      - error_msg  : str with a short reason if skipped, otherwise None
      - n_valid    : number of valid rows after cleaning
      - n_oos      : number of out-of-sample forecasts produced
    """
    # Keep minimal columns and clean
    sub = df[[x_var, dep_col]].copy()
    sub.replace([np.inf, -np.inf], np.nan, inplace=True)
    sub.dropna(inplace=True)

    n = len(sub)
    if n < min_obs + 1:
        return None, "Too few valid rows after cleaning", None, None

    start_idx = max(int(n * start_ratio), min_obs)
    if start_idx >= n:
        return None, f"start_index={start_idx} >= n={n}", None, None

    sub['Pronostico_core'] = np.nan
    sub['Pronostico_benchmark'] = np.nan

    # Expanding window
    for j in range(start_idx, n):
        train = sub.iloc[:j]
        if len(train) < min_obs:
            continue

        X = sm.add_constant(train[[x_var]], has_constant='add')
        y = train[dep_col]
        try:
            core = sm.OLS(y, X).fit()
        except Exception as e:
            return None, f"OLS core failed at j={j}: {e}", None, None

        xj = sub.iloc[j][x_var]
        if pd.isna(xj):
            continue

        sub.iat[j, sub.columns.get_loc('Pronostico_core')] = float(
            core.predict(pd.DataFrame({'const': [1.0], x_var: [xj]}))
        )

        # Benchmark: historical mean (intercept-only OLS)
        try:
            bench = sm.OLS(y, np.ones((len(y), 1))).fit()
            sub.iat[j, sub.columns.get_loc('Pronostico_benchmark')] = float(
                bench.predict(np.array([[1.0]]))
            )
        except Exception as e:
            return None, f"OLS benchmark failed at j={j}: {e}", None, None

    dd = sub.dropna(subset=['Pronostico_core', 'Pronostico_benchmark'])
    if len(dd) < min_obs:
        return None, f"Fewer than {min_obs} OOS forecasts (n={len(dd)})", None, None

    e1 = dd[dep_col] - dd['Pronostico_benchmark']
    e2 = dd[dep_col] - dd['Pronostico_core']
    mse_bench = float(np.mean(e1**2))
    mse_core  = float(np.mean(e2**2))
    if mse_bench == 0.0:
        return None, "Benchmark MSE = 0 (perfect constant), cannot compute OOS R²", None, None

    r2_oos = 1.0 - (mse_core / mse_bench)

    cw_term = e1 * (e1 - e2)
    Xc = np.ones((len(cw_term), 1))
    try:
        cw_fit = sm.OLS(cw_term.values, Xc).fit(cov_type='HAC', cov_kwds={'maxlags': hac_lags})
        cw_t = float(cw_fit.tvalues[0])
    except Exception as e:
        return None, f"Clark–West failed: {e}", None, None

    return {'dependent': dep_col, 'Variable': x_var, 'OOS_R2': r2_oos, 'CW_t': cw_t}, None, n, len(dd)


