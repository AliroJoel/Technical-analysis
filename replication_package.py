def format_jb(series):
    jb_stat, jb_p = jarque_bera(series)
    if jb_p < 0.01:
        return f"{jb_stat:.2f}***"
    elif jb_p < 0.05:
        return f"{jb_stat:.2f}**"
    elif jb_p < 0.1:
        return f"{jb_stat:.2f}*"
    else:
        return f"{jb_stat:.2f}"

# 3. Función para obtener todas las estadísticas
