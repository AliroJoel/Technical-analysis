def natural_key_x(name: str):
    """
    Natural sorting key for variables starting with 'x'.
    - Ensures x1 < x2 < ... < x10 (instead of x1, x10, x2).
    If there is no number, send it to the end.
    """
    m = re.match(r'^x(\d+)$', name)
    if m:
        return (0, int(m.group(1)))
    # If it's x_<suffix> with letters or different format, try to extract first number
    m2 = re.match(r'^x(\d+)', name)
    if m2:
        return (1, int(m2.group(1)), name)
    return (2, name)

def detectar_variables_x(df: pd.DataFrame):
    """
    Return the list of columns that start with 'x', sorted naturally.
    """
    xs = [c for c in df.columns if isinstance(c, str) and c.startswith('x')]
    xs = sorted(xs, key=natural_key_x)
    return xs

