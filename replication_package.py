def procesar_variable_estrategia(df_original, x_var, start_ratio, gamma=6, tau=0.001):
    """
    df_original: in-memory DataFrame that already contains 'y_f', 'Treasury', and x-variables.
    x_var: predictor name (e.g., 'x1', 'x2', …) or 'Benchmark_global'.
    start_ratio: fraction (0-1) to define the start index for the expanding window.
    gamma: risk aversion coefficient (default 6).
    tau: transaction cost per change in weight (default 0.001).
    """
    df = df_original.copy()

    # Force numeric types (avoid strings -> NaNs)
    cols_num = []
    for base in ['y_f', 'Treasury']:
        if base in df.columns:
            cols_num.append(base)
    cols_num += [c for c in df.columns if isinstance(c, str) and c.startswith('x')]
    for c in cols_num:
        df[c] = pd.to_numeric(df[c], errors='coerce')

    # Init columns
    for col in ['Pronostico_core', 'volatility_28d', 'peso_optimo', 'retorno_portafolio']:
        df[col] = np.nan

    # 28-day rolling variance of y_f
    df['volatility_28d'] = df['y_f'].rolling(window=28).var()

    # Start index
    start_index = int(len(df) * start_ratio)

    # Main expanding loop
    for j in range(start_index, len(df)):
        df_window = df.iloc[:j].copy()

        if x_var == "Benchmark_global":
            # Benchmark: constant-only (similar to RW ~ 0 if y_f is a return)
            df_train = df_window[['y_f']].dropna()
            if len(df_train) < 10:
                continue
            X = np.ones((len(df_train), 1))
            y = df_train['y_f']
            model = sm.OLS(y, X).fit()
            pronostico = model.predict(np.array([[1]])).item()
        else:
            # Core with x_var
            if x_var not in df.columns:
                continue  # skip if variable does not exist in df
            df_train = df_window[[x_var, 'y_f']].replace([np.inf, -np.inf], np.nan).dropna()
            if len(df_train) < 10:
                continue
            X_core = sm.add_constant(df_train[[x_var]])
            y_target = df_train['y_f']
            model = sm.OLS(y_target, X_core).fit()

            x_j = df.at[df.index[j], x_var]
            if pd.isna(x_j):
                continue
            pronostico = model.predict(np.array([[1, x_j]])).item()

        df.at[df.index[j], 'Pronostico_core'] = pronostico

        expected_return = pronostico
        expected_vol = df.at[df.index[j], 'volatility_28d']
        realized_return = df.at[df.index[j], 'y_f']
        treasury_return = df.at[df.index[j], 'Treasury']

        # Mean-variance style optimal weight (bounded [0, 1.5])
        if pd.notna(expected_return) and pd.notna(expected_vol) and expected_vol > 0:
            w_star = (1 / gamma) * (expected_return / expected_vol)
            w_star = max(0, min(1.5, w_star))
            df.at[df.index[j], 'peso_optimo'] = w_star

            # Portfolio return with transaction cost
            if j > 0 and pd.notna(df.at[df.index[j - 1], 'peso_optimo']):
                w_prev = df.at[df.index[j - 1], 'peso_optimo']
                tcost = tau * abs(w_star - w_prev)

                if pd.notna(realized_return) and pd.notna(treasury_return):
                    port_ret = (
                        w_star * realized_return
                        + (1 - w_star) * treasury_return
                        - tcost
                    )
                    df.at[df.index[j], 'retorno_portafolio'] = port_ret

    # Metrics
    serie = df['retorno_portafolio'].dropna()
    P = len(serie)
    if P > 1:
        mean_r = serie.mean()
        std_r = serie.std(ddof=1)
        sep_stat = (np.sqrt(P) * mean_r) / std_r if std_r > 0 else np.nan
        p_value = 1 - norm.cdf(sep_stat) if not np.isnan(sep_stat) else np.nan
        mean_ann = mean_r * 365
        sharpe_ann = (mean_r / std_r) * np.sqrt(365) if std_r > 0 else np.nan
    else:
        mean_r = std_r = sep_stat = p_value = mean_ann = sharpe_ann = np.nan

    resumen = pd.DataFrame({
        'Estrategia': [x_var],
        'SEP_stat': [sep_stat],
        'p_value': [p_value],
        'media_diaria': [mean_r],
        'std_diaria': [std_r],
        'media_anual': [mean_ann],
        'sharpe_anual': [sharpe_ann]
    })

    pesos = df[['peso_optimo']].copy()
    pesos['peso_BTC'] = pesos['peso_optimo']
    pesos['peso_Treasury'] = 1 - pesos['peso_BTC']
    pesos['Estrategia'] = x_var
    pesos = pesos.dropna()

    return resumen, pesos


def evaluar_todas_estrategias(
    df_original,
    variables,
    start_ratio,
    path_resultados="/content/resultados_todos.xlsx",
    path_pesos="/content/pesos_todos.xlsx",
    n_jobs=-1
):
    """
    variables: list of predictor names (e.g., ['x1', 'x2', ...]) present in df.
    path_resultados: Excel file path (metrics) in /content for Colab.
    path_pesos: if not None, also saves portfolio weights by strategy.
    n_jobs: number of processes for joblib (-1 = use all cores).
    """
    if not variables:
        raise ValueError("No variables starting with 'x' were found in the DataFrame.")

    variables_full = list(variables) + ['Benchmark_global']

    # Run in parallel
    resultados = Parallel(n_jobs=n_jobs, backend='loky')(
        delayed(procesar_variable_estrategia)(df_original, var, start_ratio)
        for var in variables_full
    )

    df_metricas = pd.concat([r[0] for r in resultados], ignore_index=True)
    df_pesos = pd.concat([r[1] for r in resultados], ignore_index=True)

    # Save results
    if path_resultados:
        df_metricas.to_excel(path_resultados, index=False)
    if path_pesos:
        df_pesos.to_excel(path_pesos, index=False)

    # Quick on-screen preview
    print("\n=== FIRST WEIGHTS BY STRATEGY ===")
    for nombre in df_pesos['Estrategia'].unique():
        print(f"\nStrategy: {nombre}")
        display(df_pesos[df_pesos['Estrategia'] == nombre].head())

    return df_metricas, df_pesos

