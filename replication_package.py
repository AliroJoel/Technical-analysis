def stars_one_sided(t: float) -> str:
    """
    Return significance stars for a one-sided Z test based on t-stat.
    Thresholds (upper tail):
      - '***' if t >= 2.3263 (1%)
      - '**'  if t >= 1.6449 (5%)
      - '*'   if t >= 1.2816 (10%)
    """
    if t >= 2.3263: return '***'  # 1%
    if t >= 1.6449: return '**'   # 5%
    if t >= 1.2816: return '*'    # 10%
    return ''


