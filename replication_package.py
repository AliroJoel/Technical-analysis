    s = series.dropna()
    mean = s.mean()
    min_ = s.min()
    max_ = s.max()
    std = s.std()
    skewness = skew(s)
    excess_kurtosis = kurtosis(s, fisher=True)
    jb_stat = format_jb(s)
    adf_stat = adfuller(s, regression='ct', autolag='AIC')[0]
    return pd.Series([
        mean, min_, max_, std, skewness, excess_kurtosis,
        jb_stat, adf_stat
    ])

