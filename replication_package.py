info("[STEP] Computing SEP statistics (one-sided, H0: mean <= 0 vs H1: mean > 0)...")
sep_results = []

for col in df_estrategias.columns:
    series = df_estrategias[col].dropna()
    P = len(series)

    if P > 1:
        mean_r = float(series.mean())
        std_r = float(series.std(ddof=1))
        sep_stat = (np.sqrt(P) * mean_r) / std_r if std_r > 0 else np.nan
